@Component
class SlettAvvisteSykmeldingerJobb(
    private val syketilfellebitRepository: SyketilfellebitRepository,
    private val leaderElection: LeaderElection,
) {
    private val log = logger()

    @Scheduled(initialDelay = 1, fixedDelay = 60 * 24, timeUnit = TimeUnit.MINUTES)
    fun run() {
        log.info("Sjekker om er leder for å slette avviste sykmeldinger")
        if (!leaderElection.isLeader()) {
            log.info("Er ikke leder, avslutter jobb")
            return
        }
        log.info("Er leder, starter jobb for å slette avviste sykmeldinger")

        val avvisteSykmeldingIder = sykmeldingIder
        val slettetTidspunkt = OffsetDateTime.parse("2025-11-06T14:30:00Z")

        val biter =
            syketilfellebitRepository
                .findAllByRessursIdIn(avvisteSykmeldingIder)
                .filter {
                    it.slettet == null
                }.map { it.copy(slettet = slettetTidspunkt) }

        log.info("skal slette ${biter.size} avviste sykmeldinger")

//        biter.forEach {
//            syketilfellebitRepository.save(it)
//            log.info("Slettet syketilfellebit med id ${it.syketilfellebitId}, ressursId ${it.ressursId}")
//        }
    }
}

